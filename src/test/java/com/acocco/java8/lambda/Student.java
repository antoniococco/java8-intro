package com.acocco.java8.lambda;

/**
 * @author acocco
 * @version $Id$
 *
 */
public class Student
{
    String firstName;

    String lastName;

    /*
     * Default constructor
     */
    Student()
    {
    }

    Student(String firstName, String lastName)
    {
        this.firstName = firstName;
        this.lastName = lastName;
    }


    public String getFirstName()
    {
        return firstName;
    }

    public String getLastName()
    {
        return lastName;
    }
}
